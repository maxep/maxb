package com.leansoft.mxjc;

/**
 * General code generation exception
 * 
 * @author bulldog
 *
 */
public class XjcCoreException extends Exception {

	private static final long serialVersionUID = 1L;

	public XjcCoreException() {
	}

	public XjcCoreException(String arg0) {
		super(arg0);
	}

	public XjcCoreException(Throwable arg0) {
		super(arg0);
	}

	public XjcCoreException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
